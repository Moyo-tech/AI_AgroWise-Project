disease_model_path = 'models/plant_disease_detector_model.pth'
disease_model = ResNet9(3, len(disease_classes))
disease_model.load_state_dict(torch.load(
    disease_model_path, map_location=torch.device('cpu')))
disease_model.eval()


# Define a function to preprocess the uploaded image
def predict_image(img, model=disease_model):
    """
    Transforms image to tensor and predicts disease label
    :params: image
    :return: prediction (string)
    """
    transform = transforms.Compose([
        transforms.Resize(256),
        transforms.ToTensor(),
    ])
    img_bytes = img.read()  # Read the content of the uploaded file as bytes
    image = Image.open(io.BytesIO(img_bytes))
    img_t = transform(image)
    img_u = torch.unsqueeze(img_t, 0)

    # Get predictions from model
    yb = model(img_u)
    # Pick index with highest probability
    _, preds = torch.max(yb, dim=1)
    prediction = disease_classes[preds[0].item()]
    # Retrieve the class label
    return prediction

def appmodel1():
    # Define the Streamlit app
    st.title('Plant Disease Detector')

    # Upload an image of a plant
    uploaded_file = st.file_uploader('Choose an image of a plant', type=['jpg', 'jpeg', 'png'])

    if uploaded_file is not None:
        # Preprocess the image
        predicted_class = predict_image(uploaded_file)
        
        # Display the image and the prediction
        st.image(uploaded_file, caption='Uploaded image', use_column_width=True)
        st.success(f"The detected disease details is : {predicted_class}")

appmodel1()

